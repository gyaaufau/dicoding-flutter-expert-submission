class FakeMovieDatabaseHelper implements MovieDatabaseHelper {
  Future<int> Function(String category)? onClearCache;
  Future<void> Function(List<MovieTable> movies, String category)?
      onInsertCacheTransaction;
  Future<List<Map<String, dynamic>>> Function(String category)? onGetCacheMovies;

  @override
  Future<int> clearCache(String category) => onClearCache!(category);

  @override
  Future<void> insertCacheTransaction(
    List<MovieTable> movies,
    String category,
  ) => onInsertCacheTransaction!(movies, category);

  @override
  Future<List<Map<String, dynamic>>> getCacheMovies(String category) =>
      onGetCacheMovies!(category);

  @override
  noSuchMethod(Invocation invocation) => super.noSuchMethod(invocation);
}

